package Practice;

public class selfpractice2 {
  public static void main(String[] args) {

    // while 연습문제 1 //
    int a = 10;
    while (a >= 0) {
      System.out.println("현재 카운트 : " + a);
      a--;
    }
    System.out.println("카운트 종료");

    // while 퀴즈 1 //
    int n = 1;
    int sum = 0;
    while (n < 100) {
      sum = sum + n;
      n++;
    }
    System.out.println(sum);

    // while 퀴즈 2 //
  }
}
